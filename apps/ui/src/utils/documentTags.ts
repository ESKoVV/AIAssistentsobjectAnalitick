import { NormalizedDocument, TopicTag } from '../types';

export const ALL_TAGS: TopicTag[] = [
  'СВО',
  'ЖКХ',
  'Транспорт',
  'Медицина',
  'Образование',
  'Безопасность',
  'Экология',
  'Экономика',
  'АПК',
  'Цифровизация',
  'Культура',
  'Спорт'
];

const keywordMap: Record<TopicTag, string[]> = {
  СВО: ['сво', 'мобилизац', 'военн'],
  ЖКХ: ['вода', 'отоплен', 'жкх', 'освещен', 'коммун'],
  Транспорт: ['автобус', 'маршрут', 'транспорт', 'метро', 'дорог'],
  Медицина: ['врач', 'больниц', 'поликлиник', 'медицин'],
  Образование: ['школ', 'университет', 'образован', 'детсад'],
  Безопасность: ['авар', 'безопас', 'чп', 'пожар', 'мчс'],
  Экология: ['эколог', 'выброс', 'загряз', 'воздух', 'свалк', 'река'],
  Экономика: ['бюджет', 'инвест', 'предприят', 'бизнес', 'налог', 'рабоч'],
  АПК: ['урожай', 'посевн', 'фермер', 'агро', 'зерн', 'сельхоз'],
  Цифровизация: ['цифров', 'госуслуг', 'интернет', 'связ', 'сервис', 'it'],
  Культура: ['театр', 'музей', 'библиотек', 'фестиваль', 'культур'],
  Спорт: ['спорт', 'стадион', 'турнир', 'трениров', 'футбол']
};

export const inferDocumentTags = (doc: NormalizedDocument): TopicTag[] => {
  const text = doc.text.toLowerCase();
  const tags = ALL_TAGS.filter((tag) => keywordMap[tag].some((word) => text.includes(word)));
  return tags.length ? tags : ['ЖКХ'];
};
